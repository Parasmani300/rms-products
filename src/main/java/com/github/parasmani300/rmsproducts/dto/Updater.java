package com.github.parasmani300.rmsproducts.dto;

public class Updater {
    String key;
    String value;

    public Updater() {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
